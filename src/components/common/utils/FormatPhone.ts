export const extractPhoneDetails = (phone: string) => {
    if (!phone.startsWith("+")) return { countryCode: null, phoneNumber: phone };
    // Extract possible country code (up to 3 digits)
    const match = phone.match(/^\+(\d{1,3})(.*)/);
    if (!match) return { countryCode: null, phoneNumber: null };
    const possibleCode = match[1];
    // Check for longest matching country code
    for (let i = 3; i >= 1; i--) {
        const code = possibleCode.slice(0, i);
        if (validCountryCodes.has(code)) {
            return {
                countryCode: `+${code}`,
                phoneNumber: phone.slice(code.length + 1) // Remove only the country code
            };
        }
    }
    return { countryCode: null, phoneNumber: phone.slice(1) }; // No valid country code found
};

export const formatPhone = (phone: string) => {
    // Extract country code and number
    if (!phone) return "";
    // Extract the phone number without country code
    const phoneTexPlain = extractPhoneDetails(phone);
    const phoneWithoutCode = phoneTexPlain.phoneNumber ?? ''
    const countryCode = phoneTexPlain.countryCode ?? ''
    let phoneNumberFormat = countryCode + " ";
    // Format display
    
    if (phoneWithoutCode.length >= 10) {
        phoneNumberFormat += phoneWithoutCode.replace(/(\d{3})(\d{3})(\d{4})/, '($1) $2-$3');
    } else if (phoneWithoutCode.length >= 7) {
        phoneNumberFormat += phoneWithoutCode.replace(/(\d{3})(\d{3})/, '($1) $2 - ');
    } else if (phoneWithoutCode.length >= 3) {
        phoneNumberFormat += phoneWithoutCode.replace(/(\d{3})/, '($1) ');
    } else {
        phoneNumberFormat += phoneWithoutCode;
    }
    return phoneNumberFormat
};

